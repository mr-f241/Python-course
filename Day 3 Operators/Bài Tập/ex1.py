# Bài 1 : Tính chu vi và diện tích hĩnh chữ nhật


height = float(input("Nhập chiều cao của hình chữ nhật: "))
width = float(input("Nhập chiều rộng của hình chữ nhật: "))

chuvi = 2 * (height + width)
dientich = height * width

print("Chu vi hình chữ nhật: ", chuvi)
print("Dien tich hình chữ nhật: ", dientich)