# Lấy phần tử đầu tiên phần tử ở giữa và phần tủ cuối cùng của danh sách
lst = ['Thành' , 'Công' , 'Văn' , 'Nhân' , 'Trân']
first_lst = lst[0]
middle_lst = len(lst) //2
last_lst = len(lst) - 1 # lst[-1]
print(f"Phần tủ đầu tiên của danh sách là : {first_lst}")
print(f"Phần tử ở giữa của danh sách là : {lst[middle_lst]}")
print(f"Phần tủ cuối cùng cua danh sách là : {lst[last_lst]}")